    private void loadTableData() {
        try {
            String sql = "SELECT CiuCod, CiuNom, CiuEstReg FROM ciudad";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();

            model.setRowCount(0);
            while (rs.next()) {
                String codigo = rs.getString("CiuCod");
                String nombre = rs.getString("CiuNom");
                String estado = rs.getString("CiuEstReg");
                model.addRow(new Object[]{codigo, nombre, estado});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al cargar datos: " + ex.getMessage());
        }
    }

