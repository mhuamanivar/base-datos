    private String generateNewCode() {
        String newCode = "1";
        try {
            String sql = "SELECT MAX(CiuCod) FROM ciudad";
            PreparedStatement pst = conn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                int maxCode = rs.getInt(1);
                newCode = String.valueOf(maxCode + 1);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error al generar nuevo c√≥digo: " + ex.getMessage());
        }
        return newCode;
    }
    
